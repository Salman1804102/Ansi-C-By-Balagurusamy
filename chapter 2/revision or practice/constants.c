#include<stdio.h>
#include<conio.h>
int main(void)
{
    printf("%d\n", 07);
    printf("%o\n", 10);
    printf("%o\n", 07);
    printf("%#o\n", 07);
    printf("%d\n", 0xABC3);
    printf("%X\n", 0XABC3);
    printf("%x\n", 0xABC3);
    printf("%X\n", 0Xabb3);
    printf("%#X\n", 0Xabb3);
    printf("\n\n%lf\n", 0.000005);
    printf("%e\n", 0.000005);
    printf("%g\n", 0.000005);
    printf("%lf\n", 5e-6);
}
