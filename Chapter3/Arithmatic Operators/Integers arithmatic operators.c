#include<stdio.h>
int main()
{
    printf("%d\n", -6 * 4);
    printf("%d\n", 6 * -4);
    printf("%d\n", -6 *- 4);
    printf("%d\n", -6 / 4);
    printf("%d\n", -6 / -4);
    printf("%d\n", -6 / 7);
    printf("%d\n", -6 / -7);
    printf("%d\n", -6 + -7);
    printf("%d\n", -6 - -7);
    printf("%d\n", -6 % 7);///results will appear according to the sign of first operand
    printf("%d\n", -6 % -7);
    printf("%d\n", 6 % -7);
    printf("%d\n", 6 % 7);
    return 0;
}
